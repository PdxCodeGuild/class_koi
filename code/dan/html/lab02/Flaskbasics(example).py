from flask import Flask
app = Flask(__name__)

@app.route('/')
def index():
    return 'Hello World'

@app.route('/goodbye')
def goodbye():
    return 'Goodbye'

@app.route('/random-message')
def random_message():
    from random import choice
    messages = [
        'Flask is fun',
        'Css is fun',
        'Html is fun',
    ]
    return choice(messages)
app.run(debug=True)













































































































































































































































































